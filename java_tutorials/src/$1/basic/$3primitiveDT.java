package $1.basic;

public class $3primitiveDT {

	public static void main(String[] args) {

		
	    byte byteValue = 10;
        System.out.println("Byte Value: " + byteValue);
        System.out.println("Byte Value: " + Byte.MAX_VALUE);
        System.out.println("Byte Value: " + Byte.MIN_VALUE);
        
	
        short shortValue = 2000;		
        System.out.println("Short Value: " + shortValue);
        System.out.println("Short Value: " + Short.MAX_VALUE);
        System.out.println("Short Value: " + Short.MIN_VALUE);
        
        
        int intValue = 300000;
        System.out.println("Integer Value: " + intValue);
        System.out.println("Integer Value: " + Integer.MAX_VALUE);
        System.out.println("Integer Value: " + Integer.MIN_VALUE);
        
        long longValue = 4000000000L;
        System.out.println("Long Value: " + longValue);
        System.out.println("Long Value: " + Long.MAX_VALUE);
        System.out.println("Long Value: " + Long.MIN_VALUE);
        
        float floatValue = 5.75f;
        System.out.println("Float Value: " + floatValue);
        System.out.println("Float Value: " + Float.MAX_VALUE);
        System.out.println("Float Value: " + Float.MIN_VALUE);
        
        double doubleValue = 19.99d;
        System.out.println("Double Value: " + doubleValue);
        System.out.println("Double Value: " + Double.MAX_VALUE);
        System.out.println("Double Value: " + Double.MIN_VALUE);
        
        char charValue = 'A';
        System.out.println("Character Value: " + charValue);
        System.out.println("Character Value: " + (int)charValue);
        
        boolean booleanValue = true;
        System.out.println("Boolean Value: " + booleanValue);
        System.out.println("Boolean Value: " + Boolean.TRUE);
        System.out.println("Boolean Value: " + Boolean.FALSE);
        
	}

}
